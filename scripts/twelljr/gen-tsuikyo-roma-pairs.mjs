/**
 * tsuikyo の input_method.ts から string 型の romaTable 行だけ抜き出し、
 * (romaji, kana) ペアを生成する（MIT / https://github.com/tomoemon/tsuikyo ）。
 * ん・っの関数エントリは packages/engine の貪欲パーサ側で wanakana フォールバックに委ねる。
 *
 * 使い方:
 *   node scripts/twelljr/gen-tsuikyo-roma-pairs.mjs [path-to-input_method.ts]
 * 引数省略時は raw.githubusercontent から取得（ネットワーク必須）。
 */
import fs from "node:fs";

const TSUIKYO_INPUT_METHOD_URL =
  "https://raw.githubusercontent.com/tomoemon/tsuikyo/master/src/input_method.ts";

async function loadText() {
  const path = process.argv[2];
  if (path) return fs.readFileSync(path, "utf8");
  const res = await fetch(TSUIKYO_INPUT_METHOD_URL);
  if (!res.ok) throw new Error(`fetch failed ${res.status}`);
  return res.text();
}

const text = await loadText();
const lines = text.split(/\n/);
const pairs = [];
let inTable = false;
for (const line of lines) {
  if (line.includes("private romaTable = {")) inTable = true;
  if (!inTable) continue;
  if (line.includes("(cont)")) break;

  const m = line.match(/^\s*'([^']+)':\s*\[([^\]]*)\]\s*,?\s*$/);
  if (!m) continue;
  const kana = m[1];
  const inner = m[2];
  const strings = [];
  const re = /'([^']*)'/g;
  let mm;
  while ((mm = re.exec(inner)) !== null) {
    strings.push(mm[1]);
  }
  for (const roma of strings) {
    if (roma) pairs.push({ roma: roma.toLowerCase(), kana });
  }
}

pairs.sort((a, b) => b.roma.length - a.roma.length);

const out = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/twelljr/gen-tsuikyo-roma-pairs.mjs
 * Source: tomoemon/tsuikyo src/input_method.ts (romaTable string entries, MIT).
 * Do not edit by hand; re-run: \`node scripts/twelljr/gen-tsuikyo-roma-pairs.mjs\`
 */
export const tsuikyoRomaToKanaPairs: readonly { readonly roma: string; readonly kana: string }[] = [
${pairs.map((p) => `  { roma: ${JSON.stringify(p.roma)}, kana: ${JSON.stringify(p.kana)} },`).join("\n")}
];
`;

const dest = new URL("../../packages/engine/src/twelljr/tsuikyoRomaToKanaPairs.gen.ts", import.meta.url);
fs.writeFileSync(dest, out, "utf8");
console.log("wrote", dest.pathname || dest.href, "pairs", pairs.length);
